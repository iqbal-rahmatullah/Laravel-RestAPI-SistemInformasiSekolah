    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            "id_matkul" => "required|string",
            "nim" => "required|string",
            "latihan_soal" => "array|max:4",
            "ulangan_harian" => "array|max:2",
            "uts" => "integer",
            "uas" => "integer"
        ]);

        $nilai = new Nilai([
            "id_matkul" => $request['id_matkul'],
            "nim" => $request['nim'],
            "latihan_soal" => $request['latihan_soal'],
            "ulangan_harian" => $request['ulangan_harian'],
            "uts" => $request['uts'],
            "uas" => $request['uas'],
        ]);

        $nilai->save();

        return response()->json("Nilai berhasil di tambahkan");
    }

